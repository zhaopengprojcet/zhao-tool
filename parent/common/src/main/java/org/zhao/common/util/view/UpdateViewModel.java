package org.zhao.common.util.view;

public class UpdateViewModel {

	private String text; 
	private String type; 
	private String name; 
	private String check;
	private String loadValue;
	private String loadValueUrl;
	private String textField;
	private String valueField;
	
	public String getTextField() {
		return textField;
	}
	public void setTextField(String textField) {
		this.textField = textField;
	}
	public String getValueField() {
		return valueField;
	}
	public void setValueField(String valueField) {
		this.valueField = valueField;
	}
	public String getText() {
		return text;
	}
	public void setText(String text) {
		this.text = text;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getCheck() {
		return check;
	}
	public void setCheck(String check) {
		this.check = check;
	}
	public String getLoadValue() {
		return loadValue;
	}
	public void setLoadValue(String loadValue) {
		this.loadValue = loadValue;
	}
	public String getLoadValueUrl() {
		return loadValueUrl;
	}
	public void setLoadValueUrl(String loadValueUrl) {
		this.loadValueUrl = loadValueUrl;
	}
}
